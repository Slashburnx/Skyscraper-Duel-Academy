function setAdminSession(v) { v ? sessionStorage.setItem('ygo-admin','true') : sessionStorage.removeItem('ygo-admin'); }

// ── Notification ───────────────────────────────────────────
let _notifTimer = null;
function notify(msg) {
  let el = document.getElementById('notif');
  if (!el) { el = document.createElement('div'); el.id='notif'; el.className='notif'; document.body.appendChild(el); }
  el.textContent = msg;
  el.style.display = 'block';
  clearTimeout(_notifTimer);
  _notifTimer = setTimeout(() => { el.style.display = 'none'; }, 3000);
}

